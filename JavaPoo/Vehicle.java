package JavaPoo;

class Vehicle {
	private String type;
	private String model;
	private String fuell;
	private String color;
	private String engine;
	private String measures;

	public Vehicle(String type, String model, String fuell, String color, String engine, String measures) {
		super();
		this.type = type;
		this.model = model;
		this.fuell = fuell;
		this.color = color;
		this.engine = engine;
		this.measures = measures;
	}

	public String getType() {
		return type;
	}

	public String getModel() {
		return model;
	}

	public String getFuell() {
		return fuell;
	}

	public String getColor() {
		return color;
	}

	public String getEngine() {
		return engine;
	}

	public String getMeasures() {
		return measures;
	}

}
 	

















